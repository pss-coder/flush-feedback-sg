export default function AnimatedFeedbackButtons({ shop, genderStr} : {
    shop: ShopDB,
    genderStr: string
  }) {


    const [toggleLanguage, setToggleLanguage] = useState(false);
    const [open, setOpen] = useState(false)

    // cooldown mechanism after submission
      // disables button that was just submitted, and re-enabled after submission
    const [cooldown, setCooldown] = useState<{ [key: string]: boolean }>({});
    const [title, setTitle] = useState("Feedback Submitted")
    const [message, setMessage] = useState("Thank you. Your feedback has been submitted successfully.")


  useEffect(() => {
    const intervalId = setInterval(() => {
      setToggleLanguage(prevToggle => !prevToggle);
    }, 1500);

    // Cleanup the interval on component unmount
    return () => clearInterval(intervalId);
  }, []);



  const labelToColumnMap: { [key: string]: string } = {
    
    'sink': 'dirty_basin',
    'toilet': 'toilet_clogged',
    'paper': 'no_more_toilet_paper',
    'soap': 'no_more_soap',
    'wet-floor': 'wet_dirty_floor',
    'dustbin': 'bin_full',
    
    // No longer in use
    'Dirty Toilet bowl': 'dirty_toilet_bowl',
    'Mirror dirty': 'dirty_mirror',
  };

  const mapSelectedButtonsToDbColumns = (selectedButton: string) => {
    
    const dbColumns = {
      bin_full: null,
      dirty_basin: null,
      dirty_toilet_bowl: null,
      dirty_mirror: null,
      no_more_soap: null,
      no_more_toilet_paper: null,
      toilet_clogged: null,
      wet_dirty_floor: null,
    } as any;

    
    const column = labelToColumnMap[selectedButton];
    if (column) {
      dbColumns[column] = true;
    }

    // Set unselected feedback to false
    Object.keys(dbColumns).forEach((key) => {
      if (dbColumns[key] === null) {
        dbColumns[key] = false;
      }
    });

    return dbColumns;
  };

  async function handleSubmit(item: string) {    
    if (cooldown[item]) {
      console.log(item + 'already submitted, thus not submitting after 2 seconds')
      setTitle("Already Submitted!")
      setMessage("Is there another item to feedback?")
      setOpen(true) // display pop-up
      return; // Prevent repeated taps
    }
    setCooldown(prev => ({ ...prev, [item]: true }));

    console.log('submitting feedback on: ' + item)
    

    setTitle("Feedback Submitted")
    setMessage("Thank you. Your feedback has been submitted successfully.")
    setOpen(true)

    // Submit item at a time
    // Handle form submission, e.g., send selectedButtons to a server
    const response = await fetch('/api/feedback', {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({ feedback: mapSelectedButtonsToDbColumns(item), shopid: shop.id, gender: genderStr}),
    });
    
    if (response.status == 200) {
      console.log('Feedback submitted successfully!');
      setOpen(false)

    } else {
      alert("something went wrong! Please try again and ensure you are connect to the network.")
      console.error('Failed to submit feedback', response);
    }

    // Set a 2-second cooldown
    setTimeout(() => {
      setOpen(false)
      setCooldown(prev => ({ ...prev, [item]: false }));
  }, 2000);

  }

  function capitalise(word:string) {
    const firstLetter = word.charAt(0);
    const firstLetterCap = firstLetter.toUpperCase()
    const remainingLetters = word.slice(1)
    const capitalizedWord = firstLetterCap + remainingLetters
    return capitalizedWord
  }

  
  return (
   <>
    <div className="w-full h-full fixed inset-0 overflow-hidden">
    {/* <DotPattern
        width={20}
        height={20}
        cx={2}
        cy={1}
        cr={1}
        // className={cn(
        //   "[mask-image:linear-gradient(to_bottom_right,white,transparent,transparent)] ",
        // )}
      /> */}
        {/* <h1 className="text-2xl font-bold">{shop.name}</h1>
      <p className="text-lg text-gray-600">Located: {shop.address}</p>
      <p className="text-lg text-gray-600">{capitalise(genderStr)} Toilet</p>
      <p className="text-lg text-black font-bold">Tap on item</p> */}
        <SvgComponent toggleLanguage={toggleLanguage} handleSubmit={handleSubmit}/>
        {/* <p className='mt-4 text-center font-light text-sm'>More language to come soon</p> */}
    </div>

        {/* Display Submitted Message  */}
        <Modal open={open} setOpen={setOpen} shopId={shop.id} gender={genderStr} useCounter={false}
        title={title}
        message={message}
        />
   </>
  )
}
